char	*ft_strncpy(char *dest, char *scr, unsigned int n)
{
	unsigned int i;

	i = -1;
	while (++i < n)
	{
		if (scr[i] == '\0')
			dest[i] = '\0';
		else
			dest[i] = scr[i];
	}
	return (dest);
}
