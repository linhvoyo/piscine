char	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int x;

	x = 0;
	if (s1[0] == '\0')
		return (s1[0] - s2[0]);
	if (s2[0] == '\0')
		return (s1[0] - s2[0]);
	while (x < n)
	{
		if (s1[x] != s2[x])
			return (s1[x] - s2[x]);
		x++;
	}
	return (0);
}
