char	ft_strcmp(char *s1, char *s2)
{
	int i;
	int x;

	i = 0;
	x = 0;
	if (s1[0] == '\0')
		return (s1[0] - s2[0]);
	if (s2[0] == '\0')
		return (s1[0] - s2[0]);
	while (s1[i])
	{
		if (s1[i] != s2[i])
			break ;
		x++;
		i++;
	}
	if (s1[x] == s2[x])
		return (0);
	if (s1[x] > s2[x])
		return (s1[x] - s2[x]);
	if (s1[x] < s2[x])
		return (s1[x] - s2[x]);
	return (0);
}
